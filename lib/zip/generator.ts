export function generateSitemapXml(scan: ScanResult): string {
  const domain = scan.businessInfo.domain || scan.url
  const baseUrl = domain.startsWith('http') ? domain : `https://${domain}`
  const today = new Date().toISOString().split('T')[0]
  const pages = scan.businessInfo.pages?.length
    ? scan.businessInfo.pages.slice(0, 20)
    : [baseUrl]

  const urls = Array.from(new Set([baseUrl, ...pages]))
    .map((page, i) => `  <url>
    <loc>${page.startsWith('http') ? page : `${baseUrl}${page}`}</loc>
    <lastmod>${today}</lastmod>
    <changefreq>${i === 0 ? 'weekly' : 'monthly'}</changefreq>
    <priority>${i === 0 ? '1.0' : '0.8'}</priority>
  </url>`)
    .join('\n')

  return `<?xml version="1.0" encoding="UTF-8"?>
<!-- sitemap.xml — generated by Queldrex AI Visibility Scanner for ${domain} -->
<!-- Upload this file to your website root so it's accessible at ${baseUrl}/sitemap.xml -->
<!-- Add additional pages following the same pattern below -->
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${urls}
</urlset>
`
}

