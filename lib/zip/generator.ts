import JSZip from 'jszip'
import type { ScanResult } from '@/lib/framework/types'

export async function generateReportZip(scan: ScanResult): Promise<ArrayBuffer> {
  const zip = new JSZip()

  zip.file('llms.txt', generateLlmsTxt(scan))
  zip.file('robots.txt', generateRobotsTxt(scan))
  zip.file('sitemap.xml', generateSitemapXml(scan))
  zip.file('schema-install.html', generateSchemaHtml(scan))
  zip.file('og-and-canonical.html', generateOgHtml(scan))
  zip.file('faq-schema.html', generateFaqSchemaHtml(scan))
  zip.file('review-schema.html', generateReviewSchemaHtml(scan))
  zip.file('recommendations.md', generateRecommendationsMd(scan))
  zip.file('report.html', generateReportHtml(scan))
  zip.file('README.txt', generateReadme(scan))

  const buffer = await zip.generateAsync({ type: 'arraybuffer', compression: 'DEFLATE' })
  return buffer
}

export function generateLlmsTxt(scan: ScanResult): string {
  return scan.generatedLlmsTxt
}

